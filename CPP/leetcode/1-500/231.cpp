class Solution {
public:
    bool isPowerOfTwo(int n) {
        /**
         *  2^0 = 1 -> 0 0 0 0
         *  2^1 = 2 -> 0 0 1 0
         *  2^2 = 4 -> 0 1 0 0
         *  2^3 = 8 -> 1 0 0 0
         *  2^4 = 16 -> 1 0 0 0 0
         * 
         * 
         *  0 1 0 0 -> 4
         *  0 0 1 1 -> 3
         * &
         * --------------
         *  0 0 0 0
         */

         return n > 0 && (n&(n-1))==0;
        
    }
};